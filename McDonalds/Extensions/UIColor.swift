import UIKit

extension UIColor {
    
    static func yellowColor() -> UIColor {
        return #colorLiteral(red: 1, green: 0.8, blue: 0, alpha: 1)
    }
    
    static func darkRedColor() -> UIColor {
        return #colorLiteral(red: 0.8588235294, green: 0, blue: 0.02745098039, alpha: 1)
    }
    
    static func lightRedColor() -> UIColor {
        return #colorLiteral(red: 1, green: 0, blue: 0, alpha: 1)
    }
}
